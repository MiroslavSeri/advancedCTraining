#ifndef FRACTALCREATOR_H_
#define FRACTALCREATOR_H_

#include <string>
#include <cstdint>
#include <memory>
#include <math.h>
#include "Zoom.h"
#include "Mandelbrot.h"
#include "Bitmap.h"
#include "ZoomList.h"
#include <vector>
#include "RGB.h"

using namespace std;

namespace caveofprograming
{

    class FractalCreator
    {
    private:
        int m_width;                   ///< Width of the fractal image
        int m_height;                  ///< Height of the fractal image
        unique_ptr<int[]> m_histogram; ///< Histogram of iteration counts
        unique_ptr<int[]> m_fractal;   ///< Stores iteration count for each pixel
        Bitmap m_bitmap;               ///< Bitmap object for image output
        ZoomList m_zoomList;           ///< List of zoom operations
        int m_total{0};                ///< Total number of iterations

        vector<int> m_ranges;      ///< Color range boundaries
        vector<RGB> m_colors;      ///< Colors for each range
        vector<int> m_rangeTotals; ///< Total pixels in each range

        bool m_bGotFirstRange{false}; ///< Flag for first color range

        /**
         * @brief Calculates the number of iterations for each pixel in the fractal.
         *
         * Iterates over all pixels, applies zoom, and computes Mandelbrot iterations.
         * Updates the histogram and fractal data arrays.
         */
        void calculateIteration();

        /**
         * @brief Calculates the total number of iterations for the fractal.
         *
         * Sums the histogram to determine the total number of iterations for all pixels.
         */
        void calculateTotalIterations();

        /**
         * @brief Calculates and prints the totals for each color range.
         *
         * Outputs the total number of pixels in each defined color range to the console.
         */
        void calculateRangeTotals();

        /**
         * @brief Draws the fractal image based on calculated iteration data.
         *
         * Computes the color for each pixel and sets it in the bitmap.
         */
        void drawFractal();

        /**
         * @brief Writes the generated fractal image to a bitmap file.
         * @param name The filename for the output bitmap image.
         */
        void writeBitmap(string name);
        void witeBitmap();

    public:
        int getRange(int iterations) const;
        /**
         * @brief Constructs a FractalCreator object with the specified width and height.
         * @param width Width of the fractal image.
         * @param height Height of the fractal image.
         */
        FractalCreator(int width, int height);

        /**
         * @brief Adds a color range for the fractal image.
         * @param rangeEnd The end value of the range (as a fraction of max iterations).
         * @param rgb The color to associate with this range.
         */
        void addRange(double rangeEnd, const RGB &rgb);

        /**
         * @brief Destructor for FractalCreator.
         *
         * Cleans up any allocated resources.
         */
        virtual ~FractalCreator();

        /**
         * @brief Executes the fractal creation process and writes the result to a bitmap file.
         * @param name The filename for the output bitmap image.
         */
        void run(string name);

        /**
         * @brief Adds a zoom to the zoom list.
         * @param zoom The zoom object specifying the zoom parameters.
         */
        void addZoom(const Zoom &zoom);
    };

} /* namespace caveofprograming */
#endif /* FRACTALCREATOR_H_ */